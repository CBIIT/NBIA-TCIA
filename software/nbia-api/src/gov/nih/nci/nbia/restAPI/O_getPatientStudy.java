package gov.nih.nci.nbia.restAPI;

import gov.nih.nci.nbia.restUtil.OviyamUtil;

import java.util.List;
import javax.servlet.http.HttpServletRequest;
import javax.ws.rs.Path;
import javax.ws.rs.GET;
import javax.ws.rs.Produces;
import javax.ws.rs.QueryParam;
import javax.ws.rs.core.Context;
import javax.ws.rs.core.MediaType;
import javax.ws.rs.core.Response;


@Path("/o/getPatientStudy")
public class O_getPatientStudy extends getData{
	private static final String[] columns={"StudyInstanceUID", "StudyDate", "StudyDescription", "AdmittingDiagnosesDescription", "StudyID", "PatientAge", "PatientID", "PatientName", "PatientBirthDate", "PatientSex", "EthnicGroup", "Collection", "SeriesCount"};
	public final static String TEXT_CSV = "text/csv";

	@Context private HttpServletRequest httpRequest;
	/**
	 * This method get a set of patient objects filtered by collection
	 * 
	 * @return String - set of patient
	 */
	@GET
	@Produces({MediaType.APPLICATION_XML, MediaType.APPLICATION_JSON, MediaType.TEXT_HTML, TEXT_CSV})
	public Response  constructResponse(@QueryParam("Collection") String collection, @QueryParam("PatientID") String patientId, @QueryParam("StudyInstanceUID") String studyInstanceUid, @QueryParam("format") String format
			, @QueryParam("oviyamId") String oviyamId, @QueryParam("wadoUrl") String wadoUrl) {
		List<String> authorizedCollections = null;
		try {
			String user=null;
			if (oviyamId!=null&&oviyamId.length()>0){
				user=OviyamUtil.getUser(oviyamId, wadoUrl);
			}
			if (user==null){
				authorizedCollections = getPublicCollections();
			} else {
				authorizedCollections = OviyamUtil.getUserCollections(user);
				if (authorizedCollections==null) {
				   authorizedCollections = getAuthorizedCollections(user);
				   OviyamUtil.setUserCollections(user, authorizedCollections);
				}
			}
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		List<Object[]> data = getPatientStudy(collection, patientId, studyInstanceUid, authorizedCollections);
		return formatResponse(format, data, columns);
	}
}
